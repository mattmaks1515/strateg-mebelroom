"""
B4 — еженедельный дайджест рынка.

Раз в неделю собирает через веб-поиск движения конкурентов по мебели в Уфе
(акции, новинки, цены) и тренды рынка РФ, отдаёт короткую сводку для Telegram.
Использует тот же «мозг» (CLAUDE.md + company.md) и веб-поиск, что и диалог.

Расписание и рассылка — в bot_worker (maybe_weekly_digest). Здесь только сборка текста.
"""

from __future__ import annotations

from advisor import ask
from brain import build_system_prompt

DIGEST_QUERY = (
    "Собери КОРОТКИЙ еженедельный дайджест рынка мебели на заказ для владельца Мебельрум (Уфа). "
    "Через веб-поиск проверь свежие движения конкурентов по Уфе: Реформа, Гауди, Бетта Барди, "
    "Бюро мебели Хусниева — новые акции, скидки, новинки, изменения цен, заметная активность в "
    "VK/на Авито/в 2ГИС. Плюс 2–3 значимых тренда рынка мебели РФ за последние недели "
    "(цены на ЛДСП/фурнитуру, спрос, законодательство).\n\n"
    "Формат (простой текст, без разметки):\n"
    "1) КОНКУРЕНТЫ — по каждому, где нашёл что-то новое: 1 строка «кто — что нового», с источником и датой. "
    "Не нашёл движений по конкуренту — пропусти его, не выдумывай.\n"
    "2) РЫНОК РФ — 2–3 коротких пункта с источником и датой.\n"
    "3) ЧТО ЭТО ЗНАЧИТ ДЛЯ НАС — 1–2 предложения: на что владельцу обратить внимание на этой неделе.\n\n"
    "Держи дайджест компактным (влезает в одно-два сообщения). Если по чему-то данных нет — так и скажи."
)


def build_digest(timeout: int = 240) -> str:
    """Возвращает готовый текст дайджеста. Бросает RuntimeError при ошибке сети/API."""
    return ask(DIGEST_QUERY, build_system_prompt(), [], timeout=timeout)
