"""
«Мозг» стратег-бота = system-prompt, собранный из файлов проекта:
  - CLAUDE.md   — роль, тон, принципы, рыночные якоря, фреймворки, формат, самопроверка
  - company.md  — актуальный контекст компании (подставляется целиком)

Файлы читаются при старте воркера и кэшируются. Правишь блок в CLAUDE.md →
git pull на сервере → systemctl restart strateg-bot → мозг обновился.

RUNTIME_NOTE честно ограничивает бота под текущий этап (B1): у него пока нет
веб-поиска и он не пишет в файлы напрямую — чтобы он не обещал того, чего не умеет.
По мере этапов B2/B3 эти ограничения снимаются правкой одной строки.
"""

from __future__ import annotations

import time

from common import CLAUDE_MD, COMPANY_MD, LOGS_DIR, OVERRIDES_MD, eprint

# Что бот УМЕЕТ на текущем этапе. Меняется по мере B2 (Битрикс) и B3 (веб-поиск).
CAPABILITIES = {
    "web_search": True,    # B3 — подключён серверный веб-поиск (локация Уфа)
    "bitrix_live": True,   # B2 — подключён: свежие агрегаты приходят в контексте запроса
    "file_write": False,   # правки company.md/decisions.md — через владельца/Claude Code
}


def _read(path) -> str:
    try:
        return path.read_text(encoding="utf-8").strip()
    except FileNotFoundError:
        eprint(f"[brain] не найден файл {path.name} — работаю без него")
        return ""


# --------------------------------------------------------------------------- #
#  Правки владельца из Telegram (команда /правка) — постоянные, высший приоритет
# --------------------------------------------------------------------------- #
def load_overrides() -> str:
    return _read(OVERRIDES_MD)


def add_override(text: str) -> None:
    """Дописать одну правку. Хранится построчно с датой, применяется сразу и после рестарта."""
    LOGS_DIR.mkdir(parents=True, exist_ok=True)
    stamp = time.strftime("%Y-%m-%d")
    with OVERRIDES_MD.open("a", encoding="utf-8") as f:
        f.write(f"- ({stamp}) {text.strip()}\n")


def clear_overrides() -> None:
    if OVERRIDES_MD.exists():
        try:
            OVERRIDES_MD.unlink()
        except OSError:
            pass


def _runtime_note() -> str:
    lines = [
        "## РАНТАЙМ (как ты сейчас работаешь — важно)",
        "",
        "Ты отвечаешь владельцу и его партнёру в чате Telegram, а не в Claude Code.",
        "Отвечай развёрнуто по формату из блока 7, но помни про среду:",
        "- ПИШИ ПО-ЧЕЛОВЕЧЕСКИ, простым понятным языком (как будто объясняешь смышлёному "
        "человеку без технической подготовки). НИКОГДА не называй внутренние технические имена: "
        "файлы (company.md, decisions.md, CLAUDE.md), названия полей и стадий Битрикса "
        "(CATEGORY_ID, STAGE_ID, WON/LOSE, воронка id=0), номера этапов (B1/B2). "
        "Вместо «в company.md указано» говори «по тому, что я знаю о вашей компании» или «по вашим цифрам». "
        "Источник — простыми словами: «по данным вашей CRM», «по тому, что вы рассказывали». "
        "Любой термин, который может быть непонятен, сначала объясни человеческими словами.",
        "- Ответы приходят в мессенджер: длинные ответы бьются на несколько сообщений — "
        "это нормально, но не растекайся, держи плотность.",
        "- НЕ используй markdown-разметку: никаких **звёздочек** для жирного, никаких "
        "#-заголовков и markdown-таблиц — Telegram их не рендерит, они выглядят как мусор. "
        "Разделы выделяй ЗАГЛАВНЫМИ буквами (ВЫВОД, РАЗБОР, РИСКИ), пункты — тире или цифрами, "
        "сравнения — строками «пункт — значение», а не таблицей.",
    ]
    if CAPABILITIES["web_search"]:
        lines.append(
            "- ВЕБ-ПОИСК подключён (результаты локализованы под Уфу). Используй его для конкурентов "
            "(2ГИС, Яндекс.Карты, VK, Авито, сайты), цен на материалы, трендов рынка РФ — всего, что "
            "меняется и требует свежих данных. Каждый факт из поиска давай С ИСТОЧНИКОМ И ДАТОЙ. "
            "Рейтинги/отзывы/цены могли измениться — помечай это. Не нашёл в поиске — так и скажи, "
            "не заполняй пробел выдумкой. Поиск платный и не мгновенный — ищи по делу, не по любому пустяку."
        )
    else:
        lines.append(
            "- ВЕБ-ПОИСК пока НЕ подключён. Если для ответа нужен свежий факт из интернета "
            "(конкуренты, цены, рынок) — не выдумывай: скажи, что это требует веб-поиска, "
            "который включат на следующем этапе, и дай, что можешь, из общих знаний с пометкой «оценка»."
        )
    if CAPABILITIES["bitrix_live"]:
        lines.append(
            "- БИТРИКС ПОДКЛЮЧЁН. Перед вопросом тебе передаётся блок «АКТУАЛЬНЫЕ ДАННЫЕ ИЗ БИТРИКСА» "
            "со свежими цифрами (выручка, договоры, средний чек, лиды по месяцам). Для чисел опирайся "
            "на него, а не на возможно устаревшие цифры из company.md. Метод выручки — по дате договора, "
            "воронка САЛОН. Текущий месяц неполный — учитывай это, не сравнивай его в лоб с полными месяцами."
        )
    else:
        lines.append(
            "- ЖИВОЙ БИТРИКС пока НЕ подключён. Цифры бери из блока «КОНТЕКСТ КОМПАНИИ» ниже. "
            "Где стоит [уточнить в Битрикс] — так и говори, что точной цифры пока нет."
        )
    if not CAPABILITIES["file_write"]:
        lines.append(
            "- Ты НЕ редактируешь файлы сам. Если предлагаешь внести факт в company.md или "
            "запись в лог решений — сформулируй готовый текст и попроси владельца сохранить "
            "(он сделает это в Claude Code). Не делай вид, что уже записал."
        )
    return "\n".join(lines)


def build_system_prompt() -> str:
    claude_md = _read(CLAUDE_MD)
    company_md = _read(COMPANY_MD)
    overrides = load_overrides()
    parts = [
        claude_md,
        "\n\n---\n\n",
        _runtime_note(),
        "\n\n---\n\n",
        "## КОНТЕКСТ КОМПАНИИ (актуальное содержимое company.md)\n\n",
        company_md or "_(company.md пуст)_",
    ]
    if overrides:
        parts += [
            "\n\n---\n\n",
            "## ПРАВКИ ВЛАДЕЛЬЦА — ВЫСШИЙ ПРИОРИТЕТ\n\n",
            "Владелец дал их прямо в Telegram. Они ГЛАВНЕЕ общих правил выше: если правка "
            "противоречит инструкции — выполняй правку. Соблюдай их всегда:\n\n",
            overrides,
        ]
    return "".join(parts)
